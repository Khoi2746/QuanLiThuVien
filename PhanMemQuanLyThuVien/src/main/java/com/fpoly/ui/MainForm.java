private void phanQuyen() {
    int role = currentUser.getRoleID();
    // Admin = 1 -> Full quy·ªÅn
    if (role == 1) {
        return;
    }

    // Th·ªß th∆∞ = 2 -> ·∫®n tab qu·∫£n l√Ω t√†i kho·∫£n
    if (role == 2) {
        removeTabIfExists("Trang Qu·∫£n L√≠ T√†i Kho·∫£n");
        removeTabIfExists("M∆∞·ª£n S√°ch");
        removeTabIfExists("Tr·∫£ S√°ch");
        removeTabIfExists("Gia H·∫°n M∆∞·ª£n");
        
    }

    // Member = 3 -> Ch·ªâ gi·ªØ l·∫°i tab M∆∞·ª£n S√°ch
    if (role == 3) {
        removeTabIfExists("Trang Qu·∫£n L√≠ T√†i Kho·∫£n");
        removeTabIfExists("B√°o C√°o Th·ªëng K√™");
        removeTabIfExists("Duy·ªát Y√™u C·∫ßu M∆∞·ª£n/Tr·∫£ S√°ch");
        removeTabIfExists("Qu·∫£n L√≠ Kho S√°ch");
        // Tab M∆∞·ª£n S√°ch ƒë∆∞·ª£c gi·ªØ l·∫°i
    }
}

private void removeTabIfExists(String title) {
    for (int i = 0; i < tabMain.getTabCount(); i++) {
        if (tabMain.getTitleAt(i).equalsIgnoreCase(title)) {
            tabMain.remove(i);
            break;
        }
    }
}

