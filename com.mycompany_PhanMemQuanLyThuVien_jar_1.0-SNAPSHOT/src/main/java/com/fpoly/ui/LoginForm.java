    public void login() {
    String username = txtUser.getText().trim();
    String password = new String(txtPassword.getPassword()).trim();

    // Ki·ªÉm tra nh·∫≠p li·ªáu
    if (username.isEmpty() || password.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui l√≤ng nh·∫≠p ƒë·∫ßy ƒë·ªß t√™n ƒëƒÉng nh·∫≠p v√† m·∫≠t kh·∫©u!");
        return;
    }

    boolean success = XAuth.login(username, password);

    if (success) {

        // ---- L·∫•y t√™n Role ----
        String roleName = "";
        int role = XAuth.currentUser.getRoleID();
        switch (role) {
            case 1:
                roleName = "Admin";
                break;
            case 2:
                roleName = "Th·ªß Th∆∞"; 
                break;
            case 3:
                roleName = "Member"; 
                break;
            default:
                roleName = "Kh√¥ng x√°c ƒë·ªãnh";
        }


        // ---- Th√¥ng b√°o ƒëƒÉng nh·∫≠p ----
        javax.swing.JOptionPane.showMessageDialog(this,
                "ƒêƒÉng nh·∫≠p th√†nh c√¥ng! Ch√†o " + XAuth.currentUser.getFullName() + " (" + roleName + ")"
        ); 

        // üëâ D√πng duy nh·∫•t 1 form ch√≠nh
        MainForm main = new MainForm(XAuth.currentUser);
        main.setVisible(true);
        this.dispose(); // ƒê√≥ng LoginForm

    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Sai t√™n ƒëƒÉng nh·∫≠p ho·∫∑c m·∫≠t kh·∫©u!");
    }
}
