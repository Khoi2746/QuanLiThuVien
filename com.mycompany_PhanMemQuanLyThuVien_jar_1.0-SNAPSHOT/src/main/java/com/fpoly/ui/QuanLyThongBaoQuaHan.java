       private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblQuaHan.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ
        try {
            List<QLQuaHan> list = dao.getAll();
            for (QLQuaHan x : list) {
                model.addRow(new Object[]{
                        x.getMaPhieu(),
                        x.getKhachHang(),
                        x.getTenSach(),
                        x.getHanTra(),
                        x.getTrangThai(),
                        x.getGhiChu()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi load dữ liệu: " + e.getMessage());
        }
    }
       
